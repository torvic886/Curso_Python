#Conocer Tipos de Datos.
valor = "Eduardo"
tipo = type(valor) # tipo:  <class 'str'>
print('tipo: ',tipo)

valor = 2
tipo = type(valor) # tipo:  <class 'int'>
print('tipo: ',tipo)

valor = 3.1
tipo = type(valor) # tipo:  <class 'float'>
print('tipo: ',tipo)

valor = True
tipo = type(valor) # tipo:  <class 'bool'>
print('tipo: ',tipo)